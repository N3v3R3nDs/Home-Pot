export const STRINGS: Record<Lang, typeof en> = { en, no };
export type StringKey = keyof typeof en;

function format(template: string, vars?: Record<string, string | number>): string {
  if (!vars) return template;
  return template.replace(/\{(\w+)\}/g, (_, k) => String(vars[k] ?? `{${k}}`));
}

/** React hook returning a translation function bound to current language. */
export function useT() {
  const lang = useSettings((s) => s.language);
  return (key: StringKey, vars?: Record<string, string | number>): string => {
    const dict = STRINGS[lang] ?? STRINGS.en;
    const template = (dict as Record<string, string>)[key] ?? (STRINGS.en as Record<string, string>)[key] ?? key;
    return format(template, vars);
  };
}
